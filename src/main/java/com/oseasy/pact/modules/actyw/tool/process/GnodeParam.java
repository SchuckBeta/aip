package com.oseasy.pact.modules.actyw.tool.process;

public class GnodeParam {
    private int size;
    private int endSize;

    public GnodeParam() {
        super();
    }
    public GnodeParam(int size) {
        super();
        this.size = size;
    }
    public int getSize() {
        return size;
    }
    public void setSize(int size) {
        this.size = size;
    }
    public int getEndSize() {
        return endSize;
    }
    public void setEndSize(int endSize) {
        this.endSize = endSize;
    }
}
